//  CASES:

//camelCase.
let fruitName = 'Apple';
console.log(fruitName);

//snake_case.
let fruit_name = 'Pine Apple';
console.log(fruit_name);

//PascalCase.
let FruitName = 'Custard Apple';
console.log(FruitName);

// Cases in type string.
let daysName = "Monday";
let days_name = "Tuesday";
let DaysName = "Wednesday";
console.log(daysName, days_name, DaysName);

// Cases in type number.
let personAge = 15;
let person_age = 20;
let PersonAge = 25;
console.log(personAge, person_age, PersonAge);

// Cases in type boolean.
let isStudent = true;
let is_student = false;
let IsStudent = true;
console.log(isStudent, is_student, IsStudent);



